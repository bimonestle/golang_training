package main

import "fmt"

func main() {
	var a = "initial"
	fmt.Println(a)

	var b, c int = 10, 13
	fmt.Println(b, c)

	var d = false
	fmt.Println(d)

	var e int
	fmt.Println(e)

	f := "ape"
	fmt.Println(f)

	g, h := "german", "high"
	fmt.Println(g, h)

	i, j := 2, 3.17
	fmt.Println(i, j)

	var k, l float32 = 10, 10.05
	fmt.Println(k, l)
}
